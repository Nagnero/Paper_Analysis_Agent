import assert from 'node:assert/strict';
import { readFileSync } from 'node:fs';

const read = file => readFileSync(file, 'utf8');

const index = read('public/index.html');
const css = read('public/app.css');
const app = read('public/app.js');
const server = read('server.js');
const fileManager = read('core/fileManager.js');
const promptStore = read('core/promptStore.js');
const llmConfig = read('core/llmConfig.js');
const coreAgent = read('agents/coreInsight.js');
const corePrompt = read('prompts/coreInsight.md');

// UI contract: the left workspace is split into Chat and Analysis without removing chat.
assert.match(index, /id="chatTabBtn"/, 'Chat tab button must exist');
assert.match(index, /id="analysisTabBtn"/, 'Analysis tab button must exist');
assert.match(index, />대화<\/button>/, 'Chat tab label must be Korean');
assert.match(index, />핵심 분석<\/button>/, 'Analysis tab label must be Korean');
assert.match(index, /id="analysisPane"/, 'Analysis tab panel must exist');
assert.match(index, /id="analysisMatrixRoot"/, 'Analysis matrix root must exist');
assert.match(index, /id="messages"/, 'Existing chat messages stream must stay present');
assert.match(index, /id="promptCoreInsight"/, 'settings must expose the core insight prompt');
assert.match(index, /data-role="coreInsight"/, 'settings must expose the core insight LLM role');

// Styling contract: Analysis is a first-class panel with a compact core-insight table, not a chat bubble.
assert.match(css, /\.workspace-tabs/, 'workspace tabs must be styled');
assert.match(css, /#chatPane\.analysis-active \.composer/, 'composer should be hidden while reading Analysis');
assert.match(css, /\.core-insight-table/, 'core insight table must be styled');
assert.match(css, /\.core-claim-text/, 'core claim text must be visually emphasized');
assert.match(css, /\.core-kind-badge/, 'core insight kind must be visible');
assert.match(css, /\.matrix-evidence-row/, 'evidence controls must be part of matrix rows');
assert.match(css, /\.analysis-error/, 'core insight generation errors must be visible');
assert.doesNotMatch(css, /\.analysis-matrix-card/, 'Analysis should not default to one card per claim');

// Frontend contract: matrix is generated by the separate Core Insight Agent and preserves PDF evidence jumps.
assert.match(app, /workspaceTab: 'chat'/, 'app state must track active left-panel tab');
assert.match(app, /function setWorkspaceTab\(tab\)/, 'workspace tab switching must be explicit');
assert.match(app, /currentCoreInsights: null/, 'app state must keep generated core insights');
assert.match(app, /function normalizeCoreInsights\(coreInsights\)/, 'UI must render coreInsight JSON');
assert.match(app, /function generateCoreInsights\(\)/, 'Analysis tab must be able to trigger the separate agent');
assert.match(app, /\/api\/library\/papers\/\$\{state\.currentPaperId\}\/core-insights/, 'frontend must call the core insights API');
assert.match(app, /핵심 주장 한눈 요약/, 'Analysis panel must label the core-insight summary clearly');
assert.match(app, /핵심 분석 생성/, 'empty Analysis tab must expose a generation button');
assert.match(app, /<span class="ref-quote">원문 근거 보기<\/span>/, 'evidence cell should not expose long English quotes by default');
assert.match(app, /onEvidenceClick\(claim\.evidenceQuote, claim\)/, 'matrix evidence buttons must reuse PDF jump/highlight behavior');
assert.match(app, /state\.currentVerifiedClaims = msg\.verifiedClaims/, 'completed analysis must feed matrix state');
assert.match(app, /state\.currentCoreInsights = analysis\?\.coreInsights \|\| null/, 'loaded papers must restore saved core insights');
assert.match(app, /renderAnalysisMatrix\(\)/, 'matrix renderer must be called when state changes');
assert.match(app, /<th>구분<\/th>/, 'table must include a Korean kind column');
assert.match(app, /<th>핵심 주장 \/ 철학<\/th>/, 'table must include a core philosophy/claim column');
assert.match(app, /<th>근거<\/th>/, 'table must include an evidence column');
assert.match(app, /<th>한계 \/ 주의점<\/th>/, 'table must include a caveat column');

// Backend contract: a separate agent writes core-insights.json without DB migration or pipeline coupling.
assert.match(server, /import \{ run as runCoreInsight \} from '\.\/agents\/coreInsight\.js'/, 'server must import the separate core insight agent');
assert.match(server, /function handleLibraryPaperCoreInsights/, 'server must expose a core insights handler');
assert.match(server, /\/core-insights/, 'router must expose /core-insights');
assert.match(server, /llmConfig\.getRole\('coreInsight'\)/, 'core insight agent must have its own LLM role');
assert.match(server, /fileManager\.writeCoreInsights/, 'server must persist generated core insights');
assert.match(server, /coreInsights: files\.coreInsights/, 'library GET must return saved core insights');
assert.match(fileManager, /core-insights\.json/, 'core insights must be saved as an analysis file');
assert.match(fileManager, /function writeCoreInsights/, 'file manager must expose core insight persistence');
assert.match(promptStore, /coreInsight/, 'prompt store must load the core insight prompt');
assert.match(llmConfig, /coreInsight/, 'LLM config must include a core insight role');

// Agent/prompt contract: JSON table content is written in Korean and grounded by claimIds.
assert.match(coreAgent, /callLLMJson/, 'core insight agent must use JSON mode');
assert.match(coreAgent, /sanitizeInsights/, 'core insight agent must sanitize LLM output');
assert.match(coreAgent, /buildCitationRefMap/, 'core insight claimIds must be restricted to citable claims');
assert.match(corePrompt, /모든 사용자 표시 필드\(kindKo, claimKo, evidenceKo, caveatKo\)는 한국어로 작성한다/, 'prompt must require Korean table fields');
assert.match(corePrompt, /supported 또는 partially_supported 이면서 evidenceQuote가 있는 claim만 claimIds에 넣는다/, 'prompt must require grounded claimIds');
assert.match(corePrompt, /기본 3~5개로 제한한다/, 'prompt must keep the table compact');

console.log('analysis-matrix panel verification passed');
